    private void loadData(List<NhaSanXuatCustomModel> listSanPhams) {
        dtm.setRowCount(0);
        for (NhaSanXuatCustomModel nsx : listSanPhams) {
            dtm.addRow(nsx.todataRow());
        }
    }

    private void fill(int index) {
        NhaSanXuatCustomModel nsx = listNsxCustom.get(index);
        lbID.setText(nsx.getId());
        txtMa.setText(nsx.getMa());
        txtTen.setText(nsx.getTen());
    }

