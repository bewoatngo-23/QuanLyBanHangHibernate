    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        NhaSanXuat sp = new NhaSanXuat(ma, ten);
        JOptionPane.showMessageDialog(this, NsxService.add(sp));
        listNsxCustom = NsxService.getAllCustom();
        loadData(listNsxCustom);
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tbHienThi.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng");
        } else {
            NhaSanXuatCustomModel sp = listNsxCustom.get(row);
            String ma = sp.getMa();
            JOptionPane.showMessageDialog(this, NsxService.delete(ma));
            listNsxCustom = NsxService.getAllCustom();
            loadData(listNsxCustom);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int row = tbHienThi.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng");
        } else {
            NhaSanXuatCustomModel nsx = listNsxCustom.get(row);
            String ma = nsx.getId();
            NhaSanXuat sp = new NhaSanXuat();
            sp.setMa(txtMa.getText());
            sp.setTen(txtTen.getText());
            JOptionPane.showMessageDialog(this, NsxService.update(sp, ma));
            listNsxCustom = NsxService.getAllCustom();
            loadData(listNsxCustom);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

