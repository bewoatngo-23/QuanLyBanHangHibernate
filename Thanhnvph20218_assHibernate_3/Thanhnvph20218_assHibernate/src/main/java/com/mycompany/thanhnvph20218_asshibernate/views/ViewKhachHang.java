    private void loadData(List<KhachHangCustomModel> listKhachHang) {
        dtm.setRowCount(0);
        for (KhachHangCustomModel listKhachhHang : listKhachHang) {
            dtm.addRow(listKhachhHang.todataRow());
        }
    }

    private void fillData(int index) {
        KhachHangCustomModel kh = listKhachHangCustom.get(index);
        lbId.setText(kh.getId());
        txtMa.setText(kh.getMa());
        txtTen.setText(kh.getTen());
        txtTenDem.setText(kh.getTenDem());
        txtHo.setText(kh.getHo());
        txtNgaySinh.setText(String.valueOf(kh.getNgaySinh()));
        txtDiaChi.setText(kh.getDiaChi());
        txtSdt.setText(kh.getSdt());
        txtMatKhau.setText(kh.getMatKhau());
        txtThanhPho.setText(kh.getThanhPho());
        txtQuocGia.setText(kh.getQuocGia());
    }

