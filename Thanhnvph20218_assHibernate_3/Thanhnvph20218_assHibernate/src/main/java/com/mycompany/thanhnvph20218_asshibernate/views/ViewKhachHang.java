    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int row = tbHienThi.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng");
        } else {
            KhachHangCustomModel khachHang = listKhachHangCustom.get(row);
            String id = khachHang.getId();
            KhachHang kh = new KhachHang();
            kh.setMa(txtMa.getText());
            kh.setTen(txtTen.getText());
            kh.setTenDem(txtTenDem.getText());
            kh.setHo(txtHo.getText());
            kh.setNgaySinh(Date.valueOf(txtNgaySinh.getText()));
            kh.setSdt(txtSdt.getText());
            kh.setDiaChi(txtDiaChi.getText());
            kh.setThanhPho(txtThanhPho.getText());
            kh.setQuocGia(txtQuocGia.getText());
            kh.setMatKhau(txtMatKhau.getText());
            JOptionPane.showMessageDialog(this, khachHangServiceImpl.update(kh, id));
            listKhachHangCustom = khachHangServiceImpl.getAllCustom();
            loadData(listKhachHangCustom);

        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tbHienThi.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng");
        } else {
            KhachHangCustomModel kh = listKhachHangCustom.get(row);
            String ma = kh.getMa();
            JOptionPane.showMessageDialog(this, khachHangServiceImpl.delete(ma));
            listKhachHangCustom = khachHangServiceImpl.getAllCustom();
            loadData(listKhachHangCustom);

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

