    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String tenDem = txtTenDem.getText();
        String ho = txtHo.getText();
        String sdt = txtSdt.getText();
        String diaChi = txtDiaChi.getText();
        String matKhau = txtMatKhau.getText();
        String thanhPho = txtThanhPho.getText();
        String ngaySinh = txtNgaySinh.getText();
        String quocGia = txtQuocGia.getText();
        KhachHang kh = new KhachHang(ma, ten, tenDem, ho, Date.valueOf(ngaySinh), sdt, diaChi, thanhPho, quocGia, matKhau);
        JOptionPane.showMessageDialog(this, khachHangServiceImpl.add(kh));
        listKhachHangCustom = khachHangServiceImpl.getAllCustom();
        loadData(listKhachHangCustom);

    }//GEN-LAST:event_btnAddActionPerformed

