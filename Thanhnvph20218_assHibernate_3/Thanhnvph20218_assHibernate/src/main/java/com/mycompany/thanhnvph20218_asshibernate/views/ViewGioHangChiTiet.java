    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tbHienThi.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng");
        } else {
            GioHangChiTiet ghct = listGioHangCT.get(row);
            String idGh = ghct.getGioHang().getId();
            String idCtsp = ghct.getChiTietSP().getId();
            JOptionPane.showMessageDialog(this, GioHangCTService.delete(idGh, idCtsp));
            listGioHangCT = GioHangCTService.getAll();
            loadData(listGioHangCT);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int row = tbHienThi.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng");
        } else {
            GioHangChiTiet ghct = listGioHangCT.get(row);
            String idGh = ghct.getGioHang().getId();
            String idCtsp = ghct.getChiTietSP().getId();
            ghct.setSoLuong(Integer.valueOf(txtSoLuong.getText()));
            ghct.setDonGia(BigDecimal.valueOf(Double.valueOf(txtDonGia.getText())));
            ghct.setDonGiaKhiGiam(BigDecimal.valueOf(Double.valueOf(txtDonGiaKhiGiam.getText())));
            String idGioHang = cbbIdGioHang.getSelectedItem().toString();
            String idCTSP = cbbIdChiTietSP.getSelectedItem().toString();
            GioHang gioHang = GioHangService.getOne(idGioHang);
            ghct.setGioHang(gioHang);
            ChiTietSanPham chiTietSP = ChiTietSPService.getOne(idCTSP);
            ghct.setChiTietSP(chiTietSP);
            JOptionPane.showMessageDialog(this, GioHangCTService.update(ghct, idGh, idCtsp));
            listGioHangCT = GioHangCTService.getAll();
            loadData(listGioHangCT);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

