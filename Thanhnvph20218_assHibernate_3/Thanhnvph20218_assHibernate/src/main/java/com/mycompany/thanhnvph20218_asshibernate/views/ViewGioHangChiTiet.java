    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String idGh = cbbIdGioHang.getSelectedItem().toString();
        String idCTSP = cbbIdChiTietSP.getSelectedItem().toString();
        GioHang gioHang = GioHangService.getOne(idGh);
        ChiTietSanPham chiTietSP = ChiTietSPService.getOne(idCTSP);
        String soLuong = txtSoLuong.getText();
        String donGia = txtDonGia.getText();
        String donGiaKhiGiam = txtDonGiaKhiGiam.getText();
        GioHangChiTiet ghct = new GioHangChiTiet(gioHang, chiTietSP, Integer.valueOf(soLuong), BigDecimal.valueOf(Double.valueOf(donGia)), BigDecimal.valueOf(Double.valueOf(donGiaKhiGiam)));
        JOptionPane.showMessageDialog(this, GioHangCTService.add(ghct));
        listGioHangCT = GioHangCTService.getAll();
        loadData(listGioHangCT);
    }//GEN-LAST:event_btnAddActionPerformed

