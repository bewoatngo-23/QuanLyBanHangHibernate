    private void loadData(List<GioHangChiTiet> listGioHangCTs) {
        dtm.setRowCount(0);
        for (GioHangChiTiet hdct : listGioHangCTs) {
            dtm.addRow(hdct.todataRow());
        }
    }

    private void fill(int index) {
        GioHangChiTiet hdct = listGioHangCT.get(index);
        cbbIdGioHang.setSelectedItem(hdct.getGioHang().getId());
        cbbIdChiTietSP.setSelectedItem(hdct.getChiTietSP().getId());
        txtSoLuong.setText(String.valueOf(hdct.getSoLuong()));
        txtDonGia.setText(String.valueOf(hdct.getDonGia()));
        txtDonGiaKhiGiam.setText(String.valueOf(hdct.getDonGiaKhiGiam()));
    }

